public class VariablesDemo {
    public static void main(String[] args) {
        // Declaring and initializing different types of variables

        // Integer type: Used to store whole numbers without decimals
        int myInt = 10;
        System.out.println("Integer: " + myInt);

        // Floating point type: Used to store numbers with fractional parts
        float myFloat = 10.5f;
        System.out.println("Float: " + myFloat);

        // Double type: Used to store larger numbers with fractional parts
        double myDouble = 20.5;
        System.out.println("Double: " + myDouble);

        // Character type: Used to store single characters
        char myChar = 'A';
        System.out.println("Character: " + myChar);

        // Boolean type: Used to store true or false values
        boolean myBoolean = true;
        System.out.println("Boolean: " + myBoolean);

        // String type (not a primitive type, but commonly used): Used to store sequences of characters
        String myString = "Namaste Java!";
        System.out.println("String: " + myString);
    }
}